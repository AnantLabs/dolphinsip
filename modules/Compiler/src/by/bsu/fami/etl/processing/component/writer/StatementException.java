package by.bsu.fami.etl.processing.component.writer;

public class StatementException extends Exception {

	private static final long serialVersionUID = -1064216356292244629L;

	public StatementException() {
	}

	public StatementException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StatementException(String arg0) {
		super(arg0);
	}

	public StatementException(Throwable arg0) {
		super(arg0);
	}

}
